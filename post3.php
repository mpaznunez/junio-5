rurales contenidas dentro del límite administrativo del territorio distrital.
<br>La estrategia es vista por los pobladores, como una herramienta poderosa
que proyecta su inclusión efectiva en el medio urbano, sin perder sus
condiciones básicas de subsistencia, según los líderes y gestores de la
iniciativa:
<br>“(…) A la política de ruralidad le metimos todo el empeño. Llegamos pensar que el actual
alcalde, Lucho Garzón (Luis Eduardo Garzón (2004 - 2007), no se iba poner la camiseta de
los campesinos en la ciudad (…) es una oportunidad para reivindicar lo que queda de ruralidad
y de recursos naturales en la ciudad (…) Esta política nos conviene y nos sirve para proteger
nuestras veredas y fomentar otros sistemas de producción más acordes con el medio ambiente.
<br>Somos conscientes que estamos frente a grandes intereses, mire por ejemplo el tema del agua,
los bosques, la explotación minera para la construcción y por ahí mismito la expansión de
la ciudad. Ahora que legislo la política hay que retomar los procesos con la comunidad y
