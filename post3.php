de comprensión de lo rural que aquí seguimos, acoge la inclusión de la
dimensión ambiental como ámbito determinante de las complejas
interrelaciones entre la actividad humana y su entorno natural. Escenario
desde el cual, debe encaminarse la promoción del desarrollo bajo una noción
de mejoramiento cualitativo de los niveles de bienestar social en el largo plazo
