ambiental, crecimiento económico productivo y bienestar y equidad social, que permita la transición
hacia un modelo de gestión sostenible, participativo que garantice a los habitantes de las áreas rurales
las capacidades, activos y oportunidades que ofrece la pertenencia a la región más dinámica y rica
del país. Identidad y culturas campesinas. Garantizar las condiciones democráticas a todos los
grupos sociales y poblacionales para el ejercicio de sus derechos y de sus expresiones culturales y
patrimonio cultural, en la perspectiva de la interculturalidad. Institucionalidad democrática. Generar
una cultura de ciudadanía activa con efectiva participación de actores sociales e institucionales, en
