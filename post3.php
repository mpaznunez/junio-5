<hr>· Adaptabilidad social
<hr>Los procesos y capacidades de los pobladores rurales para desenvolverse
eficazmente entre los distintos niveles institucionales, se ha visto fuertemente
afectada, a consecuencia de la no resolución de los conflictos sobre la
propiedad y el uso de la tierra. Asunto que se ha manifestado bajo un carácter
impositivo, luego de declararse jurídicamente en varias localidades, la disposición
