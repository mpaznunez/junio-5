<hr>2. De lo conurbano a la revalorización y adaptabilidad de los
espacios rurales
<hr>El entramado social y ambiental descrito, nos lleva a reconocer la expresión
silenciosa de espacios y poblaciones que dentro de la estructura de la ciudad,
han procurado salvaguardar sus orígenes históricos, sus formas de apropiación,
producción, y protección de los recursos naturales. Son las gentes del
medio rural que aún habitando los bordes de la ciudad, exigen derechos y
reconstruyen sus relaciones con el medio urbano. En este campo de acción,
consideramos que se encuentran las pistas para reivindicar una vinculación
emergente entre el medio rural y urbano en la ciudad de Bogotá, otorgando así
un sentido de planificación ascendente y real que contribuya a la configuración
de una ciudad ecológica y socialmente sustentable.
Desde esta perspectiva, estos territorios están inmersos en una doble
dinámica de ocupación, con lo cual nos referimos a franjas entre la periferia
urbana y las áreas definidas como suburbanas en las que habitan comunidades
propiamente rurales, donde todavía no hay una gran densificación y persiste
un loteo intenso de pequeñas parcelas, las cuales comparten sus medios de
producción con residencias de recreo de habitantes urbanos, centros
habitacionales de interés social o, incluso, sectores dormitorios de empleados
urbanos.
<br>Por tanto, este estudio propone la revalorización de lo rural en la ciudad,
basándonos para ello en las siguientes consideraciones: su contribución al
espacio urbano es lugar de preservación, equilibrio ecológico, patrimonio
cultural, depositario de actividades productivas no agrícolas, un modelo
propio de comunidad y desarrollo, una gestión del suelo basada en derechos
y deberes, una economía incluyente a partir de cohesión social y territorial, una
base ciudadana que ha procurado representación e incidencia en las organizaciones
públicas. Al respecto podemos considerar que nos encontramos,
frente a la urgente necesidad de redefinir, aquellos espacios rurales que no
pueden restringirse ni dejar de existir por el avance de la urbanización, como
lo confirma LLAMBÍ (2004) “(…) en zonas anteriormente consideradas marginales
para las tendencias del crecimiento y que por causas de la expansión
urbana han terminado como espacios de residenciales”.
Por tanto, proponemos concebir estos lugares de cohesión rural-urbana,
como espacios de conurbación, caracterizados por la vocación agrícola y las
formas de vida rural que en las periferias de una ciudad, estarían definiendo
un territorio, que a la vez es discontinuo y compuesto, marcado por una
superposición, no siempre congruente de los centros de decisión.
Desde esta perspectiva, la inclusión del medio rural a la configuración
urbana, es indagada en este estudio, dentro de un marco desde el cual,
buscamos revelar, mediante la identificación de procesos sociales de adaptabilidad,
la configuración de un territorio que se enfrenta y resiste a la presión
de la ciudad, pero que al mismo tiempo, garantiza la viabilidad medioambiental,
económica, cultural y política de su desarrollo. De esta manera, proponemos
caracterizar las capacidades de adaptabilidad de los espacios y pobladores
rurales, siguiendo para ello las premisas que nutren los procesos de evaluación
de la política europea de desarrollo rural a saber5 :
♦ Adaptabilidad histórica y espacial: concebida como la capacidad y el
reconocimiento colectivo de la memoria comunitaria y las transformaciones
espaciales ejercidas por los agentes sociales que revelan antecedentes
temporales y espaciales desde los cuales se define la configuración de un
territorio.
<hr>♦ Adaptabilidad económica: entendida como la capacidad de los actores
sociales para producir y mantener el máximo de valor sobre la capacidad
productiva en el territorio, mediante el refuerzo de vínculos entre sectores,
combinando activos para valorizar el carácter específico de los productos
y servicios locales.
<hr>♦ Adaptabilidad medioambiental: deducida como la capacidad de los
agentes sociales para valorizar su entorno, haciendo del mismo, un
elemento “distintivo”, garantizando al mismo tiempo, la conservación y
renovación de sus recursos naturales y patrimoniales.
<hr>♦ Adaptabilidad social: entendida como la capacidad de los agentes
sociales a fin de actuar, eficazmente y de manera conjunta, sobre la base
de una concepción consensuada y fomentada por la concertación entre los
distintos niveles y sectores del aparato institucional.
<hr>♦ Adaptabilidad en el contexto global: derivada de la capacidad de los
agentes sociales para situarse relación a los “otros” territorios y con el
mundo exterior.
<br>En este sentido entendemos lo rural, siguiendo los aportes de FRANCISCO
ENTRENA DURÁN (1998), como un “concepto en evolución constante”, que se
retrae del retraso interpretativo al que ha estado sujeto a causas de la
imposición del modelo de desarrollo urbano industrial, el cual lo califica como
un concepto residual, autárquico y atrasado. Sobre estos argumentos, coincidimos
en afirmar que lo rural hoy, representa un proceso social imprecisamente
concebido y convencionalmente relacionado con el sector primario de la
economía. Pero, dadas las condiciones del crecimiento, los factores del
