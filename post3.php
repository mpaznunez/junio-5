¿dónde se ha visto una ciudad en el mundo que proteja al mismo tiempos sus recursos naturales
y convenga esta iniciativa con la misma comunidad rural? este es un logro que debemos
aprovechar al máximo”. (Tomado de varios relatos de habitantes localidades rurales en
desarrollo del trabajo de campo de la investigación. Julio 2007).
<hr>• Adaptabilidad al contexto global
<hr>Ante las limitaciones, fortalezas y oportunidades que los pobladores reconocen
como evidencias de su capacidad para adaptarse a nuevos contextos, sus
iniciativas demuestran el profundo interés por dar un sentido de sostenibilidad
al entorno rural en la ciudad de Bogotá. Por tanto y a manera de reflexión y
recomendaciones, ilustramos a continuación, los resultados de un ejercicio de
prospectiva, realizado con los pobladores rurales, en el cual se destacan las
principales percepciones y retos para afrontar su futuro en el contexto de la
globalización y de las condiciones básicas para su desarrollo:
la planeación, gestión, seguimiento y control ciudadano al desarrollo rural, que garantice la
integración de la ruralidad a la estructura institucional y de participación del distrito; avanzar en las
transformaciones institucionales para una gestión adecuada a la nueva ruralidad”. Tomado de:
Alcaldía Mayor de Bogotá. Política Pública de Ruralidad de Distrital Capital. Bogotá: Alcaldía
Mayor de Bogotá, 2006.
<br>♦ Tanto los pobladores rurales como urbanos deben reconocerse como
pertenecientes a la ciudad y cada uno tiene un papel importante en los
procesos de sostenibilidad del ecosistema.
<br>♦ Los procesos educativos y de formación con las comunidades tienen que
sensibilizar y concienciar a todos los pobladores que son habitantes de
una ciudad y a la vez de un territorio.
<br>♦ Debe redefinirse la legitimidad en el uso de los ecosistemas estratégicos
a fin de instaurar en ellos usos no agrícolas que generen estabilidad para
las comunidades y conservación de la biodiversidad, la cual incluye a la
población humana y no humana.
<br>♦ No puede seguirse propiciando la desocupación de las zonas rurales sobre
la idea de vender las tierras sobre la amenaza de la expropiación. Esto
refleja una falla del proceso de descentralización y autonomía local que
se promulga pero no se aplica por la constitución de los Estados.
<br>♦ Enriquecer e integrar nuevas visiones para el desarrollo de políticas de
ruralidad y asimismo reglamentar su gestión en todas las ciudades del
mundo.
<br>♦ Declarar zonas de reserva campesina a las franjas de asentamiento
poblacional que se localizan en las periferias de las ciudades y definirlos
como espacios destinados para la conservación de cuencas agrológicas
mediante la elaboración de procesos de planificación predial que integren
y legitimen a su vez las trayectorias históricas y socioeconómicas de sus
pobladores.
<br>♦ Debe transformarse la idea que se tiene del desarrollo rural, al parecer de
las comunidades y funcionarios que abogan por los temas de ruralidad,
este proceso está mal entendido, la gente lo asocia con: “(…) construcciones,
vías, maquinaria, canalizar las quebradas, urbanizar (…). El proceso
del desarrollo rural debe entenderse como una posibilidad de conexión
entre las comunidades rurales mediante relaciones económicas y sociales
mucho más estables con el medio urbano.
<br>♦ El desarrollo debe pensarse primordialmente en armonía con el entorno
y para las comunidades rurales asentadas en dichos lugares. Por lo tanto,
se sugiere:
<br>• Propiciar procesos de producción de bajo impacto ambiental.
<br>• Capacitación en manejos alternativos del agua.
<br>• Compensaciones económicas especiales a los habitantes rurales por
la conservación de las fuentes hídricas.
<br>• Obligación y sanción para el cuidado de los recursos naturales tanto
de los habitantes de los barrios aledaños, como de los habitantes de
las veredas en las localidades.
<hr>Conclusiones
<hr>Las formas de adaptabilidad de los espacios rurales en la periferia de las
ciudades deben ser catalogadas como procesos microsociales de conurbación.
Esto demanda respuestas cada vez más eficientes y legítimas, e implican el
aumento de las capacidades locales en materia de previsión y pensamiento
estratégico para el abordaje del desarrollo por parte de las instituciones y de
su concreción ascendente en el desarrollo comunitario. Por tanto, de ello se
sugiere, la generación de escenarios en los que se reconozcan los intereses
locales y se discuta la compensación del crecimiento económico de estas
zonas, la equidad en la distribución de la riqueza, la sostenibilidad de los
recursos naturales y el fomento a la participación ciudadana. Dicha perspectiva
debe considerar aquellas sinergias que promovidas desde lo local
podrían: “(…) aumentar la capacidad colectiva para realizar acciones en
común sobre la base de una misma interpretación de la realidad y de sus
posibilidades de cambio (…) Estableciendo dicha premisa valorativa, entonces,
naturalmente sí podríamos hablar de desarrollo en un territorio, ya que él
configuraría aspectos tales como:
“libertad, democracia, justicia, paz, solidaridad, igualdad, ética, heterogeneidad y alteridad, sin
los cuales es impensable el desarrollo general, y otro número de valores singulares, propios del
territorio en cuestión, que son los valores que confieren una identidad, la que unifica hacia
adentro y distingue y separa hacia afuera; sin este segundo conjunto no será posible conformar
una fuerte cooperación y solidaridad interna”. (BOISIER, 2003).
Lo anterior sugiere reconocer que este tipo de juicios, debe definir valores
de singularidad en el contexto y en el lugar que se habita. Lo cual implica una
definición rural territorial que debe involucrar y reconocer a:
“Las personas y su subjetividad, no como un recurso adicional, sino como un requisito
indispensable; La subjetividad social, no construida desde refugios y defensas frente a la
globalización, sino potenciar su capacidad de manejarla; La reivindicación de la subjetividad
colectiva como objetivo y motor del desarrollo no debe derivar en un populismo voluntarista;
y la toma de decisiones para el desarrollo, no puede ser tecnocrática, pues involucra
incertidumbre, respecto a la diversidad y discernimiento publico”. (BOISIER, 2003).
Las evidencias encontradas en este estudio nos llevan a concluir que las
áreas de conurbación tiene entonces una argumentación positiva, con lo cual
podemos mencionar que se plantea una contradicción, entre la postura
científica tradicional, la cual defiende la existencia de un límite urbano más o
menos rígido, condicionado por las reglas del mercado, donde las actividades
agrícolas y las formas de vida rural se presentan como marginales; y los
hallazgos encontrados en este estudio, los cuales enfatizan en una nueva
valoración de los espacios y pobladores rurales como agentes dinámicos del
desarrollo y del ordenamiento territorial de las ciudades.</p>

