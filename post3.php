recursos naturales. Asunto que revela un detrimento de los asentamientos
humanos que históricamente han ocupado estos territorios. La naturaleza
inconsulta de este hecho, ha generado resentimiento de las poblaciones frente
a las entidades nacionales especialmente, dado el cruce de intereses entre las
instituciones que abogan por la sola conservación sin las comunidades y
aquellas que consideran alternativas técnicas que excluyan a los campesinos
de los procesos de preservación y producción del medio ambiente. Lo anterior
se ha debido a que, en medio de las presiones legales y continuas declaraciones
públicas de presionar para desocupar estas zonas, las entidades distritales
continúan proporcionando asistencia técnica y aportando recursos, a fin de
contrarrestar las condiciones de pobreza de las poblaciones. Al respecto
mencionan sus líderes:
