· Adaptabilidad ambiental
Los procesos de adaptabilidad medioambiental, en las zonas rurales, han sido
expresión de continuos conflictos de origen múltiple que trascienden la escala
local, regional, nacional e internacional. En ese sentido, los usos del suelo rural
de Bogotá, manifiestan una serie de conflictos, dentro de los cuales se
destacan:
<br>1. Problemas de invasión de áreas rurales, ya sea por el establecimiento de
actividades no rurales, como lo es el área del relleno sanitario Doña Juana
y la recepción de población vulnerable que reproduce el desarrollo de
barrios subnormales no planificados.
<br>2. Ingreso a la ciudad de población víctima del conflicto armado. En ese
sentido, la guerra es un problema al cual Bogotá no es ajena.
<br>3. El impacto de políticas regionales y nacionales que modifican el contexto
local rural de la ciudad. Políticas como el Tratado de Libre Comercio
(TLC), los impactos de la reciente Ley Forestal y el proyecto de Ley de
Agua (Alcaldía Mayor de Bogotá, 2006), son ejemplos de esta incidencia
global-local.
<br>4. Los diferentes niveles de intervención de organización y jerarquías
administrativas, generan restricciones en la ejecución de acciones en
beneficio de las zonas rurales.
<br>5. De acuerdo con la Alcaldía Mayor de Bogotá (2006) existen conflictos de
ordenamiento entre la nación y el Distrito: “...en el manejo y normatividad
de una porción significativa rural (…) hay una desarticulación funcional
entre el área rural y urbana del Distrito que crea una relación asimétrica
que castiga a la zona rural, en particular en los temas de expansión
urbana, ordenamiento de áreas de actividad minera y de usos para
disposición final de residuos sólidos”. (Alcaldía Mayor de Bogotá, 2006).
<br>6. Intereses de conservación de entidades como la Corporación Autónoma
Regional con injerencia departamental en Cundinamarca (CAR) sobre los
intereses de desarrollo agrícola de los campesinos e incluso de la proyección
social de las alcaldías respectivas: “La importancia ambiental del
conjunto del territorio rural ha sido priorizada en normas establecidas
desde una perspectiva de conservación en áreas de reserva para la
prestación de servicios a la zona urbana, en particular como área
disponible para la expansión, lo cual impide el desarrollo sostenible y
endógeno”. (Alcaldía Mayor de Bogotá, 2006).
En ese orden de ideas, se puede afirmar que la mayor dificultad en
equilibrar los procesos de adaptabilidad medioambiental, obedece a las
disparidades y desencuentro entre la producción - conservación que promueven
las instituciones para hacer cumplir la política que establece como
fundamento: “El ordenamiento del territorio rural del Distrito Capital basado
en el fortalecimiento de sus dos funciones principales: la conservación de los
valores y servicios ambientales concentrados en sus ecosistemas estratégicos,
y el mejoramiento de la productividad y sostenibilidad de la actividad
agropecuaria”. (Alcaldía Mayor de Bogotá, 2000). Por tanto, el punto clave
es mantener una producción agropecuaria sostenible bajo niveles de productividad
óptimos para el productor y a la vez que se conserven no sólo áreas,
sino funciones ecosistémicas de las que se beneficiaría todo el Distrito.
A continuación se presenta un Diagrama Causal respecto a las relaciones
entre los principales conflictos de uso del suelo a manera de explicación de la
insostenibilidad de los sistemas productivos. Como se puede apreciar en el
Gráfico 1, existen tres ciclos de retroalimentación que funcionan como
“trampas de pobreza” (PERRINGS, 1998), dirigiendo los sistemas productivos
hacia el deterioro por reducción de la productividad y daños a la estructura
ecosistémica que los sostiene.
El punto central es la percepción de riesgo medio ambiental de los
campesinos, la cual genera dos efectos importantes: el uso intensivo de la tierra
y la expansión de la frontera agrícola. El uso intensivo de la tierra genera por
un lado el incremento de agroquímicos, aumentando los costos de producción,
y por tanto, disminuye la rentabilidad de la actividad agrícola; por el otro lado,
el uso intensivo manifestado como siembras más frecuentes y menor tiempo
de descanso de la tierra, genera una disminución importante en la fertilidad del
suelo y por tanto, de la producción agrícola. Cuando se disminuye la
producción agrícola (en cantidad o calidad) y la rentabilidad se reduce, la
percepción de riesgo del campesino aumenta por temor a la pérdida en los
ciclos de producción.
