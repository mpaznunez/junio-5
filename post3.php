                                      <div class="cuerpo p-4"> <p>Resumen
<hr>El presente artículo expone los principales hallazgos y conclusiones obtenidos en razón del
proyecto de investigación titulado: Los procesos sociales de adaptabilidad de los espacios
rurales en la ciudad de Bogotá. Retos y perspectivas para el desarrollo del territorio rural en
áreas de conurbación. El interés que subyace a este trabajo, es el de revelar el vacío que existe
entre los procesos de adaptabilidad histórico-espacial, económico, social y ambiental, desde
los cuales se manifiestan diversas formas de concentración/desagregación del medio rural en
los espacios que bordean la periferia en la ciudad de Bogotá. El trabajo sugiere la necesidad
de valorar las unidades espaciales urbano-rurales, denominadas aquí: áreas de conurbación,desde las cuales se propone definir zonas coherentes de ordenación territorial, interacción
social, desarrollo y conservación de recursos naturales.
<hr>Palabras clave: adaptabilidad urbano-rural, territorios rurales, conurbación, políticas
públicas, planificación.


