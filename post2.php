                                        Resumen
                                        <hr>Este artículo tiene dos principales objetivos. Primero, superar el debate sobre la nueva
                                        ruralidad latinoamericana mediante una reconsideración crítica de los fundamentos
                                        epistemológicos y teóricos de la sociología rural como disciplina académica. Segundo,
                                        proponer un enfoque territorial para analizar los procesos que tienen lugar en diferentes
                                        escalas (global, nacional, local), y que involucran tanto a poblaciones rurales dispersas como
                                        a localidades más densamente pobladas, en una diversidad de entornos físicos y
                                        socioeconómicos. El tiempo y el espacio, dos categorías epistemológicas abstractas, son
                                        construidas teóricamente al especificar historia y territorio como entidades ontológicas que
                                        dotan de sustancia a las relaciones rurales-urbanas. Similarmente, se otorga sustancia a
                                        diversos actores sociales al especificar su agencia en estos procesos, incluyendo una crítica
                                        de los intentos ahistóricos de desarrollar una teoría general abstracta del campesinado, en
                                        oposición al análisis histórico de los campesinos realmente existentes.
                                        <hr>Palabras clave: sociología rural, nueva ruralidad, territorio, campesinado, regímenes de
                                        bienestar, regímenes de acumulación, interdisciplinariedad.

                                        <hr>Introducción
<hr>A comienzos de la década de 1990, en algunos medios académicos latinoamericanos
se propuso la noción de nueva ruralidad, como un concepto paraguas,
con el objeto de generar, en el corto plazo, una agenda de investigación
interdisciplinaria e interinstitucional sobre las relaciones entre los macroprocesos
globales y los procesos territoriales; y, a más largo plazo, con el propósito de
contribuir a una actualización crítica de la sociología rural latinoamericana
(PÉREZ y FARAH, 2006; LLAMBÍ, 2004).
<br>En los últimos años, la propuesta dio origen a un debate sobre el contenido
y los alcances de una supuesta teoría de la nueva ruralidad (RUBIO, 2003).
Según algunos autores, la nueva ruralidad buscaba identificar “nuevos”
fenómenos y procesos; o, por el contrario, ¿era un nuevo enfoque para
describir e interpretar los “viejos” procesos de cambio del mundo rural?
(RIELLA y ROMERO, 2003). O se trata, como sostienen otros, del surgimiento de
un nuevo paradigma que haría obsoleta la categoría rural en las ciencias
sociales, ya que ésta habría perdido su valor explicativo en el actual período
de reestructuración global (DE GRAMMONT, 2004).
<br>Con el tiempo, sin embargo, se añadieron argumentos normativos o
doctrinarios sobre las implicaciones de política de la nueva ruralidad, inicialmente
una agenda exclusivamente académica, generando mayor confusión en
el debate.
<br>Una de las principales críticas que se ha esgrimido contra el enfoque de la
nueva ruralidad es su escaso desarrollo teórico. Esta ponencia se propone
sobrepasar los términos y problemas en que hasta ahora se ubicó el debate,
mediante una reflexión crítica de los fundamentos teóricos y epistemológicos
de la sociología rural como disciplina, con miras a superar la dicotomía de una
ciencia social enfocada en “lo rural” (los estudios rurales) versus otra enfocada
en “las ciudades” (los estudios urbanos), y sustituirlo por un enfoque territorial,
centrado en los procesos que tienen lugar a diferentes escalas (global,
nacional, local) y que involucran tanto a poblaciones espacialmente dispersas
como a centros poblados de diferente tamaño enmarcados en una diversidad
de entornos económico-políticos y físico-naturales.
<br>Además de esta introducción y las conclusiones, la ponencia está
estructurada en cuatro partes. En la primera se reflexiona críticamente sobre
los fundamentos ontológicos y epistemológicos de la subdisciplina. La
segunda parte se centra en la dimensión temporal (histórica), enfatizando la
necesidad de construir una teoría para el análisis de las diferentes vías y
procesos que integran la actual transición hacia la sociedad global en América
Latina. La tercera parte se centra en la construcción teórica de la agencia de los
actores sociales más relevantes en estos procesos. Ella incluye una crítica a los
intentos ahistóricos de desarrollar una teoría general del campesinado, en
contraste con los análisis históricos (aunque sustentados teóricamente) de los
campesinados realmente existentes. La cuarta parte se centra en la dimensión
espacial (territorial), enfatizando los vínculos globales-locales y urbanorurales
de los procesos actualmente en curso.
<hr>I. ¿Nuevas o viejas ruralidades? La necesidad de repensar la
sociología rural latinoamericana
<hr>1.1. ¿Cuál ruralidad?
<hr>La sociología rural fundamenta su objeto de estudio en una definición
dicotómica de las relaciones sociales: rural versus urbano. Dos conceptos
polares, aparentemente evidentes, pero imprecisos.
Ambos conceptos, para que puedan ser empleados en la construcción de
conocimientos comparables y verificables dependen del contenido sustantivo
(u ontológico) que les otorguemos. ¿Cuáles pueden ser estos contenidos?
En el discurso de la sociología rural, el concepto de ruralidad ha estado
frecuentemente asociado a tres fenómenos interrelacionados: una baja densidad
demográfica, el predominio de la agricultura en la estructura productiva
de una localidad o región, y unos rasgos culturales (valores, creencias y
conductas) diferentes a los que caracterizan a la población de las grandes
ciudades. No existe, sin embargo, un completo consenso entre los académicos.
A lo largo del tiempo, diferentes autores han enfatizado uno u otro
aspecto, o incluido en su definición varios de ellos.
Desde el punto de vista operacional (p. ej. de medición), tampoco existe
consenso. No contamos con ningún estándar internacional para esta dicotomía.
En algunos censos nacionales, el tamaño de los centros poblados o la

densidad de población por kilómetro cuadrado son los dos principales criterios
para la definición. Los censos nacionales suelen adoptar diferentes umbrales
de tamaño poblacional o de densidad demográfica a fin de adecuar el concepto
a sus características demográficas y político-administrativas específicas. Mientras
otras definiciones operativas de ruralidad enfatizan el predominio de las
actividades primarias (principalmente agrícolas) en la base económica de
determinados territorios. Pero, ¿qué ocurre cuando en un territorio supuestamente
rural la mayoría de los empleos o los ingresos agrícolas de la población
provienen de actividades no-agrícolas?
El problema se complica si, además de comparar las definiciones
operacionales de la ruralidad, incluimos en el análisis las diferentes imágenes
de la ruralidad predominantes en cada sociedad. Hasta fechas recientes, en el
imaginario colectivo, el paisaje rural en un extremo solía ser concebido como
campos cultivados; y, en el otro, como ecosistemas deshabitados, por lo tanto,
sin intervención de los seres humanos. Actualmente, sin embargo, el imaginario
de “lo rural” tiende a abarcar un amplio continuo que incluye tanto grandes
extensiones cubiertas de plástico para la siembra controlada de flores (p. ej. la
sabana de Bogotá), como hileras de galpones donde algunos animales se crían
en confinamiento en la periferia de las grandes ciudades, hasta parques
naturales y áreas protegidas con mayor o menor intervención de los agentes
estatales (FRIEDLAND, 2002).
Es evidente, por lo tanto, que la dicotomía rural-urbana está basada en una
simplificación. Como VON THÜNEN describió hace tiempo, el uso del espacio
tiende a intensificarse a medida que nos aproximamos a los grandes centros
poblados. Lo que, por supuesto, depende de múltiples factores, como la
dotación de infraestructura del territorio, los patrones históricos de asentamiento
de la población, y el acceso de las unidades productivas y los hogares
a los servicios productivos y de consumo.
Una rápida revisión de la literatura empírica sobre los procesos en curso
en las ruralidades latinoamericanas, permite identificar algunos procesos de
cambio estructural. Por una parte, cambios en la relación entre población y
territorio (por ejemplo, el surgimiento de zonas periurbanas, con transporte
diario para la población circundante versus zonas donde se localizan actividades
agrícolas y no-agrícolas a lo largo de corredores entre dos o más ciudades,
la formación de ciudades dormitorio, el desarrollo de áreas de segunda
residencia, la ocupación por industrias de espacios anteriormente agrícolas,
más el incremento de la vialidad y el transporte entre áreas urbanas y rurales
vinculando a los trabajadores a diferentes mercados laborales (PÉREZ y FARAH,
2006). Por otra parte, un proceso de des-agrarización creciente, es decir, la
pérdida de peso de las actividades agrícolas en la base económica de muchos
territorios subnacionales, con manifestaciones a nivel de los hogares en el
empleo rural y los ingresos rurales no-agrícolas (REARDON et al., 2001;
BERDEGUÉ, REARDON y ESCOBAR, 2000).
¿Qué impactos tienen estos cambios en diferentes ruralidades?
Lo importante, como señala START (2001)
“es identificar a qué tipo de ruralidad nos estamos refiriendo, y cuál es su
estadio en determinadas vías de transformación” (START, 2001:503).
Como hace más de dos décadas señalaron BUTTEL y NEWBY (1980), la
dicotomía (o continuo) rural-urbano es sólo una distinción analítica, pero no
sustantiva. Por lo que es necesario repensar la disciplina.
<hr>1.2. ¿Cuál sociología rural?
<hr>La sociología rural, argumentan BUTTEL y NEWBY (1980)
“se ha caracterizado por un pobre desarrollo teórico; o, por un desarrollo inductivo, sin un
vínculo explícito con las teorías generales sobre la sociedad y la economía”. (BUTTEL y NEWBY,
1980:5).
Y añaden:
“es imposible que exista una teoría de la sociedad rural sin una teoría de la sociedad. Por otra
parte, al ser “lo rural” una categoría espacial, no sólo la sociología rural requiere una teoría de
la sociedad sino también una teoría de la distribución espacial de la población”. (BUTTEL y NEWBY,
1980:5).
El enfoque que aquí se plantea busca establecer los vínculos entre tres
problemáticas epistemológicas que frecuentemente se encuentran disociadas
en la literatura. Uno, la teorización de las transiciones. Es decir, la
contextualización histórica de los procesos de cambio social estructurales y
sus manifestaciones territoriales. Dos, la teorización de la agencia. Es decir, el
análisis del papel de los diferentes actores sociales en estos procesos. Tres, la
teorización del espacio. Es decir, la contextualización territorial de los procesos
de cambio estructurales.
<hr>II. La contextualización histórica de los procesos territoriales
<hr>Una de las principales críticas que se hace a la sociología rural es:
“la escasez de investigaciones con perspectivas de largo plazo (HOGGART
y PANIAGUA, 2001).
Carencia que, a nuestro juicio, deriva de la ausencia de una teoría de las
transiciones.
<hr>2.1. Elementos para una teoría de las transiciones
<hr>2.1.1. La teorización del tiempo
<hr>Las categorías epistémicas tiempo y espacio sólo existen, en abstracto, en el
mundo de las matemáticas o de la lógica. Su inclusión en la construcción de
teoría depende del contenido sustantivo (ontológico) que les otorguemos. En
esta sección nos centramos en la categoría tiempo. ¿A qué procesos se refiere?,
¿cómo analizarlos?, ¿qué es nuevo y qué viejo en los procesos sociales?, ¿qué
vínculos existen entre los macroprocesos (p. ej. globales) y los microprocesos
(p. ej. locales)?
En esta sección se plantea una muy escueta revisión crítica de la economía
política (EP) clásica, y de los aportes más recientes de los enfoques
institucionalistas y de la teoría de la regulación, a fin de clarificar tanto el
explanandum (los fenómenos o procesos que se quiere explicar) como sus
explanans (el conjunto de factores que hipotéticamente buscan explicarlos).
<hr>2.1.2. Una mirada crítica a la EP clásica
<hr>¿Cuáles son los procesos históricos relevantes? ¿Cómo identificarlos?
Para MARX, el objetivo era formular un modelo teórico de alto nivel de
abstracción para explicar la tendencia secular a la producción generalizada de
mercancías, y un análisis histórico sobre las condiciones contingentes que
incidieron en el proceso de transición del feudalismo al capitalismo en Europa.
De ambos análisis derivó una prognosis sobre la inviabilidad futura del
capitalismo como resultado de las tendencias monopólicas a la concentración
del capital, y de los conflictos sociales (las luchas de clases) que estas
tendencias generarían (KATOUZIAN, 1982). Por el contrario, según interpretó
estos mismos procesos ADAM SMITH, al incrementarse la competencia, en
función de la generalización de las relaciones de mercado, se produciría una
mayor división del trabajo en la sociedad; lo que, a su vez, generaría cambios
tecnológicos, y como corolario un mayor crecimiento económico.
¿Qué retener de los clásicos? En general, es importante retener el peso que
otorgaron a temas como las asimetrías de poder, y las desigualdades de acceso
de los diferentes estratos de la población a los recursos económicos. En
particular, de MARX es importante retener su énfasis en los efectos de las
desigualdades sociales sobre los conflictos distributivos, sus manifestaciones
en los procesos de reestructuración económica y política; la tensión entre los
avances tecnológicos y las relaciones sociales; y la importancia de la acción
colectiva para resolver dichas tensiones (BOOTH, 1994; ROEMER, 1986).
¿Qué es necesario descartar? De MARX, es necesario descartar la doctrina
de las inexorables “leyes de movimiento” de la sociedad, que se apoyan tanto
en una extrapolación injustificada del evolucionismo biológico de DARWIN,
como en la ideología del progreso prevaleciente en el siglo XIX. Como
argumenta GRANOVETTER (1985), estas “leyes”, a diferencia de las predicciones
condicionales de las teorías explicativas, en realidad son profecías ya que no
especifican las condiciones para su cumplimiento. Planteamiento con el que
coincide FOUCAULT (1997, citado por BARDHAN y RAY, 2006), quien sostiene que
las sociedades cambian a través de una serie de luchas de poder, pero no existe
ninguna trayectoria predeterminada ni predecible en estos procesos.
<hr>2.1.3. Los aportes institucionalistas
<hr>Los viejos institucionalistas y la nueva economía institucional
En 1946 la publicación del libro de KARL POLANYI “La gran transformación”
revivió el debate clásico sobre los impactos del avance de la producción
generalizada de mercancías (la economía de mercado) sobre las relaciones
personalizadas de las sociedades rurales precapitalistas (BRYCESON, 2000). Los
“formalistas”, liderados por RAYMOND FIRTH, argumentaban que en una economía
de mercado todos los agentes económicos se ven obligados a asignar
recursos a diferentes objetivos; por lo que los supuestos de la economía
neoclásica también serían aplicables al análisis de las economías premercantiles.
Por el contrario, los substantivistas, liderizados por POLANYI, planteaban la
necesidad de distinguir teóricamente entre la racionalidad orientada a la
subsistencia de los productores premercantiles, y la racionalidad de los actores
sociales en una economía de mercado competitiva, en las que todos están
obligados a minimizar gastos a fin de garantizar su supervivencia (BARDHAN y
UDRY, 1999; BRYCESSON, 2000).
Más recientemente, la nueva economía institucional (NEI) buscó reconciliar
la formalidad teórica de la economía neoclásica con la riqueza de los
análisis históricos de los viejos institucionalistas. A nuestro juicio, el principal
aporte de la NEI es la definición de las instituciones como las reglas de juego
que enmarcan las relaciones de todo tipo entre los actores sociales en sus
procesos de toma de decisiones. Según los institucionalistas (viejos y nuevos),
todas las instituciones sociales, incluso los mecanismos de mercado, están
enmarcadas (un término de POLANYI retomado por GRANOVETTER), el conjunto
de regulaciones formales o informales que establecen las reglas de juego. En
las economías de mercado, la NEI plantea un continuo institucional: a nivel
macro, la interdependencia sistémica de los mercados; a nivel micro (p. ej.
local) los acuerdos (generalmente informales) entre los actores sociales en sus
frecuentes vínculos personales; y, entre ambos, las organizaciones jerárquicas
(principalmente las corporaciones privadas y las burocracias públicas) reguladas
por procedimientos administrativos internos (NORTH, 1990; WILLIAMSON
1985; ZEZZA y LLAMBÍ, 2002).
La teoría de los regímenes de acumulación
El objetivo de la teoría de la regulación, de moda durante la década de los
setenta, era superar las interpretaciones unilineales de la historia de los
clásicos, enfatizando en cada etapa de la larga evolución del capitalismo como
sistema, la existencia de diferentes regímenes de acumulación del capital, y
diferentes formas de regulación de sus relaciones económicas.
Según este enfoque, los regímenes de acumulación son sistemas de reglas
que en cada período histórico buscan resolver el problema del orden social,
identificado por HOBBES en 1651 en el Leviatán. Por lo que, el Estado-nación,
en tanto garante del orden, cumple importantes papeles en la construcción de
las bases institucionales de toda sociedad (BARDHAN y UDRY, 1999).
La teoría de los regímenes de bienestar
Más recientemente, la teoría de los regímenes de bienestar retomó los
conceptos básicos de los institucionalistas a fin de explicar la dinámica de los
capitalismos realmente existentes, con especial énfasis en los estados de
bienestar de Europa Occidental durante el siglo XX. Sus principales preguntas
son: ¿qué papeles desempeñan el estado (los gobiernos), el mercado (las
empresas privadas), y los hogares (las familias) en la provisión de seguridad
y bienestar social a los ciudadanos en los sociedades contemporáneas?; y ¿a
través de qué mecanismos se forjan los pactos sociales capaces de garantizar
el orden, tanto a nivel nacional como internacional?
En su análisis histórico del proceso de construcción de los estados-nación
europeos, ESPING-ANDERSEN (2002) identifica dos principales momentos. Por
una parte, largos períodos de consolidación de los regímenes de bienestar,
seguidos por períodos más cortos de cambio de régimen.
Según este autor, dependiendo de la estructura del Estado, y de sus
relaciones con los actores políticos más relevantes, estas coyunturas de
cambio estructural se caracterizan por una aguda competencia ideológica
entre los principales actores sociales a fin de definir las nuevas reglas de juego.
Competencia que deberá concluir con algún tipo de compromiso histórico (o
pacto social) entre las partes en conflicto, a fin de generar un nuevo régimen
de bienestar relativamente estable.
Es importante destacar también que los resultados de estos procesos de
cambio de régimen se manifiestan a diferentes escalas (nacional, sectorial y
territorial) y mediante diversas vías o trayectorias de cambio. Por ejemplo, a
escala territorial, en una estructura agraria caracterizada por grandes terratenientes
y por aldeas rurales aisladas y fragmentadas se tienden a generar
condiciones que establecen obstáculos a la transición. Como resultado, el
sistema de extracción de excedentes de la mano de obra local por los grandes
terratenientes puede perdurar largo tiempo, sin que los actores locales se vean
forzados por los mecanismos del mercado a la búsqueda de una escala óptima
para sus unidades productivas (OLIVER, 1993; BARDHAN y UDRY, 1999).
¿Cómo identificar qué es nuevo y qué es viejo en estos procesos?
“Algunos de los procesos que han sido interpretados como de cambio
estructural han estado con nosotros por un largo tiempo” (HOGGART y PANIAGUA,
2001:55).
Es posible identificar tendencias seculares (o de largo plazo) en estos
procesos. Por ejemplo, la tendencia a la privatización de los derechos de uso
sobre la tierra y su corolario: los procesos expulsión de las poblaciones rurales
de la tierra (o de descampesinización). Así como diferentes vías mediante las
cuales estos procesos se manifiestan. Por ejemplo, en Europa por la fuerza
durante los enclosures (1700-1810); y, más recientemente en Colombia y en
América Central, mediante el desalojo masivo de las poblaciones rurales de
sus tierras como resultado de guerras civiles. No obstante, en otros países, el
libre funcionamiento de los mecanismos de mercado, los cambios demográ-
ficos que derivan en mayor presión sobre la tierra, y los cambios tecnológicos
que incrementan la productividad de los competidores, frecuentemente resultan
en procesos de migración rural-urbana, cuya conclusión final generalmente
es la pérdida de los derechos de uso sobre la tierra de las antiguas
poblaciones rurales (BARDHAN y UDRY, 1999; MCMICHAEL, 2000).
Por supuesto, siempre es factible encontrar múltiples elementos de continuidad,
incluso en procesos de cambio que interpretamos como cambio de
régimen o cambio estructural. Lo que plantea la necesidad de formular
hipótesis específicas a fin de identificar hasta qué punto los fenómenos
analizados son parte o no de transformaciones estructurales de más vieja data
aún no concluidos; o, por el contrario, pueden ser interpretados como rupturas
estructurales entre un régimen y otro.
<hr>2.2. El análisis de las transiciones históricas
<hr>2.2.1. El pacto socialdemócrata en Europa y el acuerdo de BRETTON WOODS
<hr>Durante los siglos XIX y XX, los roles del Estado-nación en Europa occidental
se fueron expandiendo como resultado de la extensión de los derechos
ciudadanos y de la provisión de infraestructura y bienes públicos. Con el
tiempo, las clases trabajadoras consolidaron sus movimientos, primero luchando
por la organización sindical, y luego por plenos derechos democráticos.
A medida que se desarrollaban las industrias nacionales, también crecía
el poder de los grupos sindicales para paralizar sus operaciones a través del
derecho a la huelga, en búsqueda de mejores condiciones de empleo y
seguridad social. En diversos países y momentos, en la medida en que fueron
capaces de lograr cierto poder a través de sistemas electorales, lograron
reclamar algunos derechos: por ejemplo, el derecho a la protección por
desempleo y la seguridad social (MCMICHAEL, 2000).
Gradualmente, las demandas maximalistas de abolición de la propiedad
privada o de incrementos del salario por encima de la productividad del trabajo
de las fuerzas políticas socialdemócratas fueron abandonadas, al constatar que
podían conducir a una caída en la inversión y por lo tanto, en el empleo. Los
movimientos sociales optaron por aceptar un compromiso histórico basado en
el incremento del ahorro y de la seguridad social que permitía a los propietarios
del capital retener una parte del excedente productivo, a cambio de garantías
de inversión de una parte del excedente productivo en el mejoramiento de su
bienestar (ROEMER, 1986).
Con posterioridad a la crisis de 1930, este pacto social-demócrata dio
origen a la estrategia de desarrollo nacional. En lo económico: las políticas
keynesianas, cuyos fundamentos teóricos sugerían un activo papel para el
gobierno en la regulación de la economía con el fin de lograr altas tasas de
crecimiento y competitividad internacional. En lo social, las políticas del
estado de bienestar.
No obstante, como argumentó KEOHANE, en tanto el keynesianismo y el
estado de bienestar pudieran ser concebidos como un orden económicopolítico
nacional, este régimen sólo podía ser implementado eficientemente al
interior de un orden internacional. Bretton Woods fue un complejo pacto
social internacional entre las grandes potencias de la época, cuya finalidad era
apoyar las estrategias de desarrollo nacional con ayuda financiera, estabilidad
monetaria, y asistencia técnica y profesional.
<hr>2.2.2. Pactos sociales y desarrollos nacionales en el período de la
posguerra en América Latina
<hr>En América Latina, la estrategia de desarrollo nacional enfatizó tres
macroproyectos. En primer lugar, la industrialización por sustitución de
importaciones, orientada en los países más grandes, a satisfacer la demanda de
las clases medias urbanas, que contaban con mayor poder adquisitivo, y de
una creciente clase obrera (MCMICHAEL, 2000). En segundo lugar, el crecimiento
del mercado interno mediante la protección de las industrias domésticas, la
incorporación de los estratos de mayores ingresos de la población como
consumidores, y la integración física del territorio nacional (KAY, 2000). Por
último, la consolidación de un sector agrícola orientado al mercado interno,
mediante la adaptación al trópico de los paquetes tecnológicos desarrollados
en los países templados (BARDHAN y UDRY, 1999).
En este contexto, en la mayoría de los países, tuvo también lugar un pacto
social implícito (populista, corporativista, o simplemente redistributivista)
basado en la expectativa de que tanto el crecimiento económico como las
mejoras en la calidad de vida conducirían a mayor legitimidad del sistema
político (MCMICHAEL, 2000).
No obstante, la estrategia de desarrollo nacional tuvo lugar en un heterogéneo
contexto territorial caracterizado por diferentes estructuras agrarias y
condiciones físico-naturales, como también en condiciones económico-polí-
ticas nacionales diferentes. Por lo que, particularmente en la agricultura, no
existió una única vía en el proceso general de transición, sino varias.
<hr>2.2.3. La actual transición hacia un régimen global en América Latina
<hr>El abandono del tratado de Bretton Woods por Estados Unidos en 1971 y el
inicio de la Ronda Uruguay de negociaciones en 1986, son dos importantes
hitos históricos en la actual transición hacia un régimen económico-político
global.
En América Latina, con posterioridad a 1973, fecha del golpe de Estado
en Chile, pero más claramente a partir de la crisis de la deuda externa en 1982,
los gobiernos tuvieron que aceptar las nuevas reglas de juego del régimen
global en gestación. Gradualmente la estrategia de desarrollo nacional fue
sustituida por la estrategia de apertura de las economías nacionales a los
mercados mundiales.
Hasta fecha reciente, la mayoría de las reformas políticas tuvieron como
objetivo sustituir el pacto social (populista, corporativista, o redistributivista)
que caracterizó a los estados latinoamericanos en el marco de la estrategia de
desarrollo nacional por un pacto social más acorde con las reglas de juego del
régimen global en gestación.
Paralelamente, el nuevo pacto social internacional postula la construcción
de una nueva institucionalidad supranacional a fin de lograr una eficiente
regulación de los mercados mundiales. Esto dio origen, a nivel mundial, a un
ambiente institucional complejo integrado por múltiples acuerdos y agencias
multilaterales, los tratados de libre comercio, y los carteles de las corporaciones
transnacionales.
<hr>III. ¿Nuevos o viejos campesinismos? La agencia de los actores
<hr>relevantes en estos procesos
Esta sección está centrada en la identificación de algunas herramientas
epistemológicas y teóricas indispensables para el análisis de los fundamentos
‘micro’ (la capacidad de agencia de los actores sociales) en los procesos de
transición. El objetivo es la explicación de los cambios estructurales en
función de la interacción entre una multiplicidad de actores en contextos
históricos y territoriales específicos. ¿Quiénes son los actores sociales relevantes
en todos estos procesos en cada período histórico? ¿Sólo son los campesinos,
entendidos como agricultores familiares total o parcialmente vinculados
al mercado? ¿Qué papeles han desempeñado otros actores (individuales o
colectivos), como por ejemplo las agencias gubernamentales, los agronegocios,
y las asociaciones de productores? ¿Cómo construir teóricamente la agencia
de los actores sociales relevantes a fin de explicar su participación en un
fenómeno o proceso específico?
3.1. La teorización de la agencia individual y colectiva
Según ELSTER (1998), la unidad elemental de explicación en las ciencias
sociales es la agencia intencional de los seres humanos actuando individual o
colectivamente.
“Los hombres hacen la historia”, escribió MARX, “pero con los materiales
que les proporciona la sociedad” (CARLOS MARX, 1852: Tesis sobre Feuerbach).
En el marco de una agenda para la refundación de la sociología rural, esto
nos conduce a replantear el viejo debate sobre las relaciones entre la agencia
de los actores sociales y los procesos estructurales. ¿Son explicables los
procesos sociales por las relaciones sociales (p. ej. las relaciones entre clases
en las cuales los individuos constituyen sólo soportes pasivos); o, por el
contrario, por la actividad consciente de individuos y grupos? (SAYER, 1994).
A fin de responder a esta pregunta se han propuesto dos posiciones
extremas. Por una parte, el individualismo metodológico característico de la
economía neoclásica. Por otra parte, los enfoques que enfatizan la importancia
de las restricciones institucionales o culturales, dejando poco espacio para la
libertad de acción o para la elección racional (BARDHAN y UDRY, 1999).
Existe, sin embargo, un tercer enfoque que enfatiza la inserción
(embeddedness) de la conducta individual en las relaciones sociales
(GRANOVETTER, 1985; BARDHAN y UDRY, 1999). En otras palabras, los actores no
toman decisiones y actúan como si fueran átomos, independientemente de su
contexto social. Sus intentos de actuación intencional siempre están inmersos
en sistemas de relaciones sociales concretos y cambiantes, y son restringidos
por dichas relaciones sociales.
¿Cómo explicar la acción colectiva? ¿Qué condiciones la favorecen o
impiden? ¿Cómo hacer para que suficientes individuos (con los intereses y
recursos apropiados) actúen coordinadamente en la búsqueda de un bien
público? (OLIVER y MAXWELL, 1988).
MANCUR OLSON en La Lógica de la Acción Colectiva, construyó un modelo
teórico para explicar la acción colectiva sobre la base de un dilema entre los
intereses individuales y los colectivos (OLIVER, 1993). OLSON argumentó que si
el objetivo de una acción colectiva es la provisión de un bien público, quienes
deciden no participar no podrán ser excluidos de sus beneficios, lo que —si
los actores son racionales— los conducirá a no participar, es decir, actuar en
forma oportunista.
No obstante, como señalan OLIVER y MAXWELL (1988), en realidad el dilema
del viajero gratis (free rider) es un problema de cálculo costo-beneficio. Desde
el punto de vista de quien está decidiendo participar o no, la percepción de no
ser capaz de producir una diferencia significativa en los beneficios que
realmente compense los costos. De donde concluye que, para que las acciones
colectivas sean exitosas, deberán siempre estar acompañadas de incentivos
selectivos privados. Es decir, restringidos sólo a los miembros del colectivo.
Según la teoría, una serie de condiciones afectan las posibilidades de
acciones colectivas exitosas: el tamaño del grupo, la existencia de una masa
crítica de agentes comprometidos con un proyecto, el logro de compromisos
ideológicos, el capital humano de los actores participantes, el tipo de organización,
y la implementación de sistemas de premios y castigos por el colectivo.
<hr>3.2. La teorización de los campesinados históricos
<hr>3.2.1. La noción de campesinado
<hr>La noción de campesinado no equivale a una categoría social homogénea. Los
clásicos de la economía política concebían al campesinado como una categoría
social, internamente heterogénea, que abarcaba tanto a una variedad de
trabajadores dependientes (siervos, aparceros, arrendatarios) como a peque-
ños agricultores independientes. LENIN, sobre la base de la prognosis de MARX
en relación a la tendencia hacia la polarización de las clases sociales en una
economía de mercado, argumentó que el destino irremediable de los campesinados
históricos sería convertirse en burgueses o en proletarios.
En general, en la economía política marxista, la noción de “campesinado”
define a una clase social: un actor social colectivo inserto en procesos
históricos concretos. No obstante, lo que realmente puede unificar a diferentes
actores sociales rurales, y convertirlos en un campesinado histórico (real y no
teórico), no es la existencia de una sociedad, cultura, o economía típicamente
campesinas, sino las múltiples relaciones sociales que vinculan a estos actores
con proyectos comunes, sustentados en la construcción de una identidad
colectiva.
Referirnos a los campesinos o al campesinado, en abstracto, corre el riesgo
de dotar de agencia a una categoría social (fabricada por el investigador),
perdiendo la oportunidad para construir teóricamente la agencia de los actores
sociales realmente existentes, en contextos temporales y espaciales específicos.
A nuestro juicio, los adjetivos campesino y capitalista, al referirnos a las
empresas agrícolas en una economía de mercado son poco rigurosos. Supuestamente
una finca campesina se refiere a una empresa administrada por un
hogar rural y trabajada por los miembros de la familia; mientras que una finca
capitalista se refiere a una empresa de capital privado trabajada con mano de
obra asalariada. No obstante, una finca familiar puede ser desde una empresa
que utiliza tecnología capital-intensiva y completamente orientada al mercado
hasta un lote de tierra explotado con tecnología trabajo-intensiva para la
satisfacción de las necesidades de subsistencia. Similarmente, una finca
capitalista puede ser desde una empresa mecanizada, completamente orientada
al mercado, hasta una explotación agrícola extensiva sin criterio mercantil
(HUSSAIN y TRIBE, 1983).
No obstante, en algunas sociedades, en coyunturas y en contextos territoriales
específicos, la noción de campesino ha facilitado el logro de una
identidad común a actores sociales que buscan articular sus heterogéneos
intereses en un proyecto común. Por el contrario, en otras coyunturas y
espacios, la divergencia de intereses entre estos actores sociales pudiera
predominar sobre sus convergencias. En estas ocasiones, sólo poderosos
incentivos ideológicos o proyectos (políticos, religiosos), pueden generar la
suficiente cohesión social para motivar la acción colectiva.
Desde un punto de vista antropológico, la identidad campesina es un
término “emic”. Es decir, un imaginario colectivo que permite la articulación
entre los intereses individuales y los intereses comunes de una población,
según la perspectiva normativa o cognitiva de los propios actores. Por una
parte, el elemento integrador puede ser: un territorio común, la etnicidad
(generalmente asociada a un lenguaje), la ocupación, la religión, la clase
social, el género. Dependiendo de cuáles son los principales puntos de quiebre
en la estructura social de una sociedad, estas diferencias generarán o no
identidades colectivas y alianzas entre diferentes estratos y categorías sociales.
Por otra parte, es cierto también, como argumenta SEN (2002 citado por
IBRAHIM, 2006), que generalmente los actores sociales se construyen a sí
mismos múltiples lealtades e identidades y las manejan como más convenga
de acuerdo a las circunstancias.
<hr>3.3. Las teorías del hogar rural
<hr>Las diferencias entre las empresas agrícolas familiares son tantas, que sería un
equívoco conceptual continuar subsumiendo estos grupos bajo una sola
categoría abstracta (p. ej. economía campesina, pequeña producción mercantil,
finca familiar, etc.), sólo porque mayoritariamente empleen mano de obra
familiar, o porque estén vinculadas a los mercados, independientemente de
otros criterios. En otras palabras, estos agricultores no pueden ser considerados
ni un grupo homogéneo ni una forma de producción estable.
El objetivo de la literatura sobre el hogar rural/finca es diseñar modelos
para analizar la toma de decisiones que tiene lugar en los hogares rurales
vinculados a las actividades agrícolas. Los orígenes de esta literatura se
remontan a la teoría de la economía campesina de Chayanov, reconceptualizada
con base en las contribuciones de la economía del hogar de Becker y sus
seguidores.
Esta literatura identifica al hogar (y no al individuo) como el centro del
análisis, y analiza las decisiones conjuntas del hogar sobre consumo, producción,
y asignación del tiempo de trabajo de sus miembros a diferentes
actividades. Frecuentemente, en el medio rural, la empresa familiar es una
finca, por lo que, metodológicamente, la unidad de análisis privilegiada es el
hogar rural/finca (BARDHAN y UDRY, 1999).
Según ELLIS (1993), en un contexto de mercados plenamente formados y
competitivos, la maximización del beneficio siempre es un componente de los
objetivos (la función de utilidad) del hogar rural, aunque puedan existir otras
variables en el proceso toma de decisiones (p. ej. la seguridad alimentaria).
En un contexto en el que existen “fallas de mercado” (es decir, mercados
incompletos o inexistentes) el hogar rural está obligado a mantener un balance
entre sus propias necesidades y las demandas del entorno socioeconómico en
el que está inserto. A fin de resolver este dilema los hogares rurales se ven
obligados a proseguir diferentes estrategias económicas (p. ej. la seguridad
alimentaria, la diversificación de sus actividades económicas agrícolas y noagrícolas,
y la agregación de valor a sus productos) y no-económicas.
Ante las crisis generadas por los cambios en el entorno, y dependiendo de
su poder económico y político, los más emprendedores ajustan sus estrategias,
y algunos incluso logran insertarse en los mercados más dinámicos (HOGGART
y PANIAGUA, 2001).
Los menos emprendedores, como argumentó JAMES SCOTT, autor de “Las
armas de los débiles”, no logran organizar acciones colectivas o movimientos
sociales exitosos, pero se oponen a sus opresores en formas mucho más sutiles
(como p. ej. involucrándose en pequeños robos) (BRYCESON, 2000).
<hr>3.4. La teorización de los movimientos sociales rurales
<hr>Los movimientos sociales son actores que deciden implementar formas de
acción colectiva, con algún grado de organización y con estabilidad en el
tiempo.
Uno de los méritos de la literatura teórica sobre los nuevos movimientos
sociales es que amplía nuestra comprensión sobre la acción colectiva, al
enfatizar el poder explicativo de las ideologías y de los cambios en el entorno
político para superar los dilemas de la acción colectiva (BEARD y DASGUPTA,
2006; IBRAHIM, 2006).
En particular, los movimientos de protesta se articulan alrededor de una
ideología: el mito del retorno a un orden social más justo e igualitario en el
pasado (p. ej. la sociedad rural idealizada), o la utopía de su posible creación
en el futuro (WOLF, 1966; HOGGART y PANIAGUA, 2001).
IV. La contextualización territorial de los procesos históricos
Esta parte de la ponencia discute algunas posibles herramientas para el análisis
de la dimensión espacial de la actual transición, enfatizando los vínculos
territoriales globales-locales y urbano-rurales de estos procesos. La dimensión
territorial es analizada desde tres perspectivas: a) macro-micro, es decir, los
vínculos entre los procesos globales y locales; b) comparativamente, enfatizando
el análisis de diferentes ruralidades latinoamericanas; y, c) urbano-rural,
enfatizando los vínculos entre los centros poblados y sus entornos rurales.
<hr>4.1. La teorización del espacio
<hr>El espacio, al igual que el tiempo, no existe en abstracto, sino como distancia
entre dos puntos en un universo euclidiano. Por lo que, al dotar al espacio de
contenido sustantivo entramos en el terreno del análisis territorial (físiconatural,
cultural, económico, político). Lo que, operacionalmente, se traduce
en la necesidad de construir diferentes escalas espaciales (p. ej. las unidades
de suelo en un sistema de cultivo, la superficie total de las fincas, el territorio
de una economía regional, el territorio nacional, el territorio global).
Adicionalmente, según los objetivos de política de los estados nacionales,
es posible encontrar diferentes criterios operativos para delimitar territorios
subnacionales: físico-naturales (p. ej. cuencas hidrográficas, áreas
ambientalmente protegidas, el espacio físico de ecosistemas y agroecosistemas
específicos); o niveles de gobierno político-territoriales (p. ej. el Estado, el
departamento, la provincia, el municipio).
Pero el territorio, además de una categoría analítica u operativa, es también
una construcción social según la perspectiva normativa o cognitiva de quienes
en él habitan y de sus poblaciones vecinas. Desde este punto de vista (emic),
el territorio no es un espacio físico, objetivamente existente, sino un conjunto
de relaciones sociales que da origen, y a la vez expresa una identidad y un
destino común, compartido por múltiples agentes públicos y privados. Como
construcción social generalmente está vinculado al diseño e implementación
de proyectos comunes “desde la base”, es decir, por los propios actores
sociales.
<hr>4.2. Las diferentes ruralidades latinoamericanas
<hr>Las diferencias territoriales locales son siempre el resultado de combinaciones
de factores físico-naturales y sociales. Como señalamos al inicio de la
ponencia citando a START (2001): ¿a qué tipo de ruralidad nos referimos? En
otras palabras, no existe una ruralidad, sino múltiples. Heterogeneidad rural
que depende de los criterios de análisis del investigador: p. ej. físico-naturales
(ecosistemas y recursos naturales), localización espacial, sistemas productivos
y relaciones de mercado; recursos culturales y étnicos (valores y patrimonio
histórico); y político-institucionales.
Existe una compleja relación entre los recursos naturales, tecnología, y las
relaciones sociales. Según las teorías clásicas y neoclásicas de las ventajas
comparativas estáticas la dotación de recursos naturales y la localización
explican los flujos de mercancías a través de las fronteras nacionales. En el
marco de los procesos de globalización y de las políticas de apertura generalizada
a los mercados globales, no sólo los países o sus empresas compiten sino
también los territorios subnacionales; por lo que las ventajas y desventajas de
los diferentes territorios deben formar parte de la explicación.
* La escala y aislamiento de las pequeñas comunidades rurales
Según DOUGLASS NORTH (1990), en una comunidad pequeña y cerrada, los
costos de transacción pueden ser relativamente más bajos que en una economía
de mercado, pero los costos de producción son más altos, ya que la
especialización y la división del trabajo están severamente limitados por la
dimensión de los procesos de intercambio personalizados de la pequeña
comunidad (BARDHAN y UDRY, 1999).
Típicamente, en los mercados de ámbito reducido (territoriales), ocurren
intercambios relativamente periódicos entre bienes producidos en un espacio
circundante con una plaza de mercado “central”. La división del trabajo
resultante al interior de estos territorios tiende a generar barreras de entrada,
en virtud de las cuales algunos productos disfrutan de una situación monopólica
(WOLF, 1996). Por el contrario, en un mercado ampliado, las transacciones
entre productores y consumidores tienden a no ser durables, y frecuentemente
están mediadas por intermediarios comerciales, que no involucran otro tipo de
relaciones sociales.
La dotación de infraestructura vial y de comunicaciones facilita el acceso
a los mercados de productos, a los servicios, y a los centros de poder público.
La dotación de infraestructura y servicios facilita la concentración espacial de
una población anteriormente dispersa, lo que genera economías de escala para
la prestación de mayores servicios a la población. Incluso en zonas rurales
anteriormente apartadas, el creciente acceso a las tecnologías de información
y comunicación (TIC) de territorios aislados desde otro punto de vista,
actualmente hace posible su vinculación a todo tipo de redes globales.
<hr>4.3. Vínculos entre los procesos globales y locales
<hr>Los procesos globales y las políticas nacionales no afectan de la misma manera
a los territorios subnacionales. Debido a su heterogeneidad de condiciones, y
a diferencias históricas, culturales y políticas relativamente autónomas, estos
macroprocesos tienen efectos diferenciados a escala local, por lo que suscitan
diversidad de respuestas de parte de los actores locales.
Dependiendo de sus ventajas comparativas iniciales y de la creación de
nuevas ventajas competitivas algunos territorios logran incrementar su
competitividad territorial mientras otros la pierden, lo que genera procesos de
diferenciación territorial.
A medida que se incrementa el poder de compra rural, los patrones de
consumo de la población rural gradualmente se orientan hacia los productos
y servicios urbanos. En esta misma medida, ceden las barreras de protección
generadas por las fallas de mercado rurales. A fin de competir, las actividades
rurales de transformación tienen que modernizarse o desaparecen. Esto genera
dos efectos. Por una parte, a medida que la producción no-agrícola se orienta
hacia los consumidores urbanos, la población rural tiende a convertirse en
multiespacial (entre el campo y la ciudad), dependiendo cada vez más de los
mercados de trabajo, de las remesas de dinero, del comercio, y de las redes
sociales urbanas. Por otra parte, sólo las actividades rurales que cuentan con
ventajas comparativas vis-a-vis las urbanas logran sobrevivir.
Por el contrario, a medida que avanza la urbanización, los mismos factores
que generan economías de escala, de proximidad y de aglomeración, también
conducen a la congestión urbana, incrementando los costos y agotando los
recursos. Lo que genera la expansión de las actividades urbanas hacia sus
periferias, frecuentemente a lo largo de corredores viales entre centros
poblados, a fin de aprovechar la localización de recursos más baratos. Con el
tiempo, la expansión urbana también afecta a los espacios rurales de menor
densidad demográfica, generando puntos de crecimiento de actividades noagrícolas
que se benefician de menores costos laborales y de tierra, agua y
energía más baratos (START, 2001).
<hr>4.4. La jerarquía de centros poblados y los vínculos urbano-rurales
<hr>Los territorios rurales no están definidos sólo por los vínculos con la tierra y
con el ambiente natural, sino también por sus vínculos con los centros
poblados de diferente tamaño.
Pudiéramos ilustrar estos vínculos con dos casos extremos. Uno, los
vínculos entre los territorios altiplánicos del norte del departamento de La Paz
y la ciudad de La Paz en Bolivia. Dos, Galipán, un pequeño enclave de
población rural vinculado a la agricultura y al turismo, localizado en un parque
nacional al interior del área metropolitana de Caracas.
En los territorios subnacionales existe siempre una jerarquía de centros
poblados en función de su tamaño, sus vínculos con la red de infraestructura
(vialidad, transporte, conectividad) y su jerarquía en la estructura políticoadministrativa.
De donde derivan diferentes vínculos y funciones con su
entorno rural en los mercados de productos, servicios y factores; así como
vínculos de orden político-administrativo.
En una comunidad pequeña y relativamente aislada, la división del trabajo
(y por ende la especialización productiva) generalmente están limitados por el
reducido tamaño del mercado. Por lo que, en estos pequeños mercados suelen
ocurrir intercambios relativamente periódicos entre bienes producidos en su
periferia. Por el contrario, en un contexto territorial más amplio y dinámico, los
centros poblados de tamaño mediano suelen cumplir un conjunto de funciones:
mercado para bienes y servicios rurales; abastecimiento de bienes
industriales; provisión de servicios productivos; provisión de servicios de
consumo; intermediación financiera; procesamiento de productos rurales;
empleos e ingresos no-agrícolas; y, por último pero no menos importante,
funciones de gobierno.
<hr>Conclusiones
<hr>En décadas recientes la sociedad rural latinoamericana ha sido transformada
como consecuencia de la ampliación y profundización de las relaciones de
mercado en el campo, y la integración de la agricultura a la economía mundial.
A nuestro juicio, los grandes retos de la sociología rural latinoamericana en el
actual período de globalización son.
El objeto de estudio de la disciplina debe ser el análisis de los territorios
rural-urbanos realmente existentes, no una ruralidad en abstracto.
Superar el empirismo. La necesidad de un mayor vínculo entre la teoría y la
investigación.
Para esto se necesita una guía teórica. Proponemos la gradual construcción
de una nueva economía política institucional, anclada en la historia y en el
territorio, pero evitando todo tipo de interpretaciones deterministas.
La agenda propuesta necesita profundizar la investigación sobre los nuevos
y los viejos procesos, la continuidad y el cambio, en las sociedades latinoamericanas.
La globalización no es un proceso unidimensional, sino la intersección de
múltiples procesos, cada uno de ellos con diferentes trayectorias. No existe una
trayectoria predeterminada. Es necesario evitar el simplismo de las teorías
conspirativas. Estos procesos son el resultado de múltiples actores e intereses,
lo que se manifiesta en múltiples contingencias.
Pero, como en la mayoría de los procesos sociales, en todos estos procesos
suele haber ganadores y perdedores. Lamentablemente, hasta el presente,
muchos de los perdedores se encuentran entre los pobres rurales de América
Latina. Situación que es necesario revertir mediante el diseño de políticas
públicas apropiadas.
Un elemento central en la agenda es el análisis de los actores rurales
realmente existentes. En América Latina existe una gran diversidad de campesinos
y campesinados. Como en todos los demás estratos poblacionales, en la
población rural existen activos emprendedores y pasivos buscadores de renta.
Los emprendedores generan dos tipos de proyectos: individuales y colectivos.
Son los proyectos colectivos de los emprendedores rurales los que están en
capacidad para generar procesos de desarrollo rural territorial, si cuentan con el
capital humano y el apoyo externo suficiente. No obstante, incluso los agentes
menos emprendedores dan origen a estrategias de sobrevivencia que es necesario
analizar, y que si se articulan a movimientos sociales más amplios pueden
dar lugar a cambios estructurales.
Los procesos de globalización y los procesos locales no están aislados.
Existen múltiples interdependencias que es necesario analizar. En América
Latina, en el actual contexto de globalización de mercados, es factible constatar
la tendencia a una mayor diferenciación territorial. Los mecanismos de mercado,
si no son regulados por intervenciones públicas, tenderán a producir cada vez
mayor diferenciación territorial, lo que pudiera conducir a mayores conflictos
sociales. Existe necesidad de mayor investigación sobre las políticas públicas
más conducentes a disminuir la diferenciación social y territorial.
Los centros poblados de diferente tamaño y posición en la jerarquía
político-institucional, desempeñan importantes papeles que es necesario
analizar, a fin de diseñar estas políticas públicas alternativas.
La agenda teórica y de investigación que en este artículo se propone tiene
un doble objetivo. A corto plazo, la refundación de la sociología rural
latinoamericana. A más largo plazo, contribuir al surgimiento de una ciencia
social netamente interdisciplinaria.
